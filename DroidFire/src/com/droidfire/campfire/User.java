package com.droidfire.campfire;

public class User {
	private boolean mIsAdmin;
	private String mEmail;
	private int mId;
	private String mName;
	
	public boolean isAdmin() {
		return mIsAdmin;
	}
	
	public String getEmail() {
		return mEmail;
	}
	
	public int getId() {
		return mId;
	}
	
	public String getName() {
		return mName;
	}
}
